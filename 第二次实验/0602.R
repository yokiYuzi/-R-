#中位数回归直线
X<-c(9.1,8.3,7.2,7.5,6.3,5.8,7.6,8.1,7.0,7.3,6.5,6.9,8.2,6.8,5.5);
Y<-c(8.7,9.6,6.1,8.4,6.8,5.5,7.1,8.0,6.6,7.9,7.6,7.8,9.0,7.0,6.3);
cyx=coef(lm(X~Y))
md=median(X)
x1<-X[X<=md]
x2<-X[X>md]
y1<-Y[Y<=md]
y2<-Y[Y>md]
md1=median(x1)
md2=median(x2)
mw1=median(y1)
mw2=median(y2)
beta=(mw2-mw1)/(md2-md1)
alpha<-median(X-beta*X)
x11()
plot(X,Y)
abline(alpha,beta)
abline(c(cyx),lty=2)
